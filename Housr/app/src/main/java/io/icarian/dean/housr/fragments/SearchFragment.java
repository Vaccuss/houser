    final void getData(final TextView display) {
        Log.i("getData", "Called");
        final String url = "http://192.168.99.1:3000/api";


        JsonArrayRequest jsObjRequest = new JsonArrayRequest
                (Request.Method.GET, url, null, new Response.Listener<JSONArray>() {

                    @Override
                    public void onResponse(JSONArray response) {
                        ArrayList<String> item = new ArrayList<>() ;
                        try {
                            for(int i = 0; i < response.length(); i++){
                                JSONObject jsObj = response.getJSONObject(i);
                                String itemStr = jsObj.getString("item");

                                item.add(i,itemStr);
                            }

                        } catch (JSONException e) {
                            Log.i("getData", "some errors in json");
                            item.add("Error");
                        }
                        display.setText("");
                        for (int i = 0; i < item.size(); i++){
                            display.append(item.get(i) + "\n");
                        }

                        Log.i("getData", "display should have updated");
                    }
                }, new Response.ErrorListener() {

                    @Override
                    public void onErrorResponse(VolleyError error) {
                        // TODO Auto-generated method stub
                        Log.i("getData", error.toString());

                    }
                });
        Context activityContext = getActivity();
        Requester.getInstance(activityContext).addToRequestQueue(jsObjRequest);
        Log.i("getData", "Ended");

    }
